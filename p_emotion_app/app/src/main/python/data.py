def preprocess_data(json_data):
    # 通道映射&选定的通道和采样率
    channel_names = ['Fp1', 'Fp2', 'F3', 'F4', 'F7', 'F8', 'T7', 'T8', 'C3', 'C4', 'P7', 'P8', 'P3', 'P4', 'O1', 'O2']
    channel_mapping = {f'Channel_{i + 1}': channel_names[i] for i in range(16)}
    selected_channels = ['Fp1', 'Fp2', 'F3', 'F4', 'P3', 'P4', 'O1', 'O2', 'F7', 'F8']
    sfreq = 150  # 原始采样率

    # 从 JSON 字符串创建 Pandas 数据框
    data = pd.read_json(StringIO(json_data))

    # 预处理数据
    eeg_data = data[[col for col in data.columns if 'Channel_' in col]].rename(columns=channel_mapping)

    # 创建 MNE RawArray
    info = mne.create_info(ch_names=list(channel_mapping.values()), sfreq=sfreq, ch_types='eeg')
    raw = mne.io.RawArray(eeg_data.T, info)

    # 预处理(滤波&陷波)
    raw.filter(4, 45, fir_design='firwin')
    raw.notch_filter(50, filter_length='auto')  # 使用自动计算的较短滤波器长度
    # 重采样至 128Hz
    raw.resample(128)
    raw.pick_channels(selected_channels)
    raw.set_eeg_reference()

    # 将 MNE Raw 对象保存为 JSON 格式
    eeg_data_dict = {}
    for i, ch_name in enumerate(raw.info['ch_names']):
        # 提取每个通道的数据，并存储为字典
        eeg_data_dict[ch_name] = raw.get_data(picks=[i]).flatten().tolist()

    #返回json格式的数据
    json_data = json.dumps(eeg_data_dict, ensure_ascii=False, indent=4)
    return json_data

def preprocess_and_classify_emotion(json_data):
    # 通道映射&选定的通道和采样率
    channel_names = ['Fp1', 'Fp2', 'F3', 'F4', 'F7', 'F8', 'T7', 'T8', 'C3', 'C4', 'P7', 'P8', 'P3', 'P4', 'O1', 'O2']
    channel_mapping = {f'Channel_{i + 1}': channel_names[i] for i in range(16)}
    selected_channels = ['Fp1', 'Fp2', 'F3', 'F4', 'P3', 'P4', 'O1', 'O2', 'F7', 'F8']
    sfreq = 150  # 原始采样率

    # 从 JSON 字符串创建 Pandas 数据框
    data = pd.read_json(StringIO(json_data))

    # 预处理数据
    eeg_data = data[[col for col in data.columns if 'Channel_' in col]].rename(columns=channel_mapping)
    info = mne.create_info(ch_names=list(channel_mapping.values()), sfreq=sfreq, ch_types='eeg')
    raw = mne.io.RawArray(eeg_data.T, info)
    raw.filter(4, 45, fir_design='firwin')
    raw.notch_filter(50, filter_length='auto')
    raw.resample(128)
    raw.pick_channels(selected_channels)
    raw.set_eeg_reference()

    ##特征提取
    data=raw
    freq_bands = {
        'theta': (4, 8),
        'alpha': (8, 12),
        'beta': (12, 30),
        'gamma': (30, 45)
    }

    # 滑动窗口参数
    win_length = 256  # 2秒（128Hz 下为 256 采样点）
    step = 16  # 0.125秒（128Hz 下为 16 采样点）

    sfreq = data.info['sfreq']
    data_arr = data.get_data()
    n_channels, n_times = data_arr.shape
    n_windows = (n_times - win_length) // step + 1

    features_by_band = {}
    for band, (l_freq, h_freq) in freq_bands.items():
        band_data = mne.filter.filter_data(data_arr, sfreq, l_freq=l_freq, h_freq=h_freq, verbose=False)
        band_features = np.zeros((n_windows, n_channels))
        for i in range(n_windows):
            start = i * step
            end = start + win_length
            window_data = band_data[:, start:end]
            var = np.var(window_data, axis=1)
            de_values = 0.5 * np.log(2 * np.pi * np.e * var)
            band_features[i, :] = de_values
        features_by_band[band] = band_features

    # 合并特征
    all_features = np.concatenate([features_by_band[band] for band in ['theta', 'alpha', 'beta', 'gamma']], axis=1)
    # 归一化处理
    scaler = StandardScaler()
    all_features_normalized = scaler.fit_transform(all_features)


    ##情感分类
    features=all_features_normalized
    # 定义情感标签映射
    emotion_labels = {
        0: "沮丧、悲伤",
        1: "低自信、冷漠",
        2: "焦虑、紧张",
        3: "恼怒、恐惧",
        4: "满足、平静",
        5: "放松、自信",
        6: "激动、兴奋",
        7: "快乐、幸福"
    }


    filename = join(dirname(__file__), "model.tflite")

    # 加载 TensorFlow Lite 模型

    # 使用二进制内容初始化 TensorFlow Lite 解释器
    interpreter = tflite.Interpreter(model_path=filename)
    interpreter.allocate_tensors()

    # 获取输入和输出张量
    input_details = interpreter.get_input_details()
    output_details = interpreter.get_output_details()

    # 计算特征维度
    num_channels = 10
    num_bands = 4
    feature_dim = num_channels * num_bands  # 10×4 = 40

    # 计算时间步数（有多少窗口）
    time_steps = features.shape[0]  # 每个窗口对应一个时间步

    # 初始化预测结果列表
    y_pred_classes = []

    # 对每个时间步进行预测
    for i in range(time_steps):
        # 输入数据
        input_data = features[i].reshape((1, 1, feature_dim)).astype(np.float32)
        interpreter.set_tensor(input_details[0]['index'], input_data)

        # 进行推理
        interpreter.invoke()

        # 获取输出结果
        output_data = interpreter.get_tensor(output_details[0]['index'])
        y_pred_class = np.argmax(output_data, axis=-1)
        y_pred_classes.append(y_pred_class[0])

    # 统计类别出现次数
    class_counts = Counter(y_pred_classes)  # 统计每个类别出现的次数
    total_predictions = time_steps  # 总时间步数

    # 排序
    sorted_classes = class_counts.most_common()

    # 主要情绪类别
    main_class, main_count = sorted_classes[0]
    main_emotion = emotion_labels[main_class]
    main_class_ratio = main_count / total_predictions

    # 次要情绪类别
    if len(sorted_classes) > 1:
        secondary_class, secondary_count = sorted_classes[1]
        secondary_emotion = emotion_labels[secondary_class]
        secondary_class_ratio = secondary_count / total_predictions
    else:
        secondary_emotion = "无次要情感类别"
        secondary_class_ratio = 0

    # 返回主要情绪类别及其占比，次要情绪类别及其占比
    emotion = {
        "main_emotion": main_emotion,
        "main_class_ratio": main_class_ratio,
        "secondary_emotion": secondary_emotion,
        "secondary_class_ratio": secondary_class_ratio
    }

    #转成json格式
    emotion_result=json.dumps(emotion, ensure_ascii=False, indent=4)
    return emotion_result


